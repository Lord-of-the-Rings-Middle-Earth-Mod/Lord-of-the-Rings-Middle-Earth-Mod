package me.anedhel.lotr.world;

import net.minecraft.world.gen.placementmodifier.*;

import java.util.List;

/**
 * This class is responsible for managing the placement of ores in the game world. It provides methods to create lists
 * of placement modifiers for different types of ore generation.
 */
public class ModOrePlacement {

    public static List<PlacementModifier> modifiers(PlacementModifier countModifier, PlacementModifier heightModifier) {
        return List.of(countModifier, SquarePlacementModifier.of(), heightModifier, BiomePlacementModifier.of());
    }

    public static List<PlacementModifier> modifiersWithCount(int count, PlacementModifier heightModifier) {
        return modifiers(CountPlacementModifier.of(count), heightModifier);
    }

    public static List<PlacementModifier> modifiersWithRarity(int chance, PlacementModifier heightModifiers) {
        return modifiers(RarityFilterPlacementModifier.of(chance), heightModifiers);
    }
}
