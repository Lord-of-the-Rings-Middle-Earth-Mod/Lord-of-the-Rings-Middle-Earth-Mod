package me.anedhel.lotrmiddleearth.datagen;

import me.anedhel.lotrmiddleearth.blocks.ModBlocks;
import net.fabricmc.fabric.api.datagen.v1.FabricDataOutput;
import net.fabricmc.fabric.api.datagen.v1.provider.FabricTagProvider;
import net.minecraft.registry.RegistryWrapper;
import net.minecraft.registry.tag.BlockTags;

import java.util.concurrent.CompletableFuture;

public class ModBlockTagProvider extends FabricTagProvider.BlockTagProvider {

	public ModBlockTagProvider(FabricDataOutput output,
			CompletableFuture<RegistryWrapper.WrapperLookup> registriesFuture) {
		super(output, registriesFuture);
	}

	@Override
	protected void configure(RegistryWrapper.WrapperLookup arg) {
		getOrCreateTagBuilder(BlockTags.PICKAXE_MINEABLE).add(ModBlocks.CHALK).add(ModBlocks.CHALK_BRICKS)
				.add(ModBlocks.OVERGROWN_CHALK_BRICKS).add(ModBlocks.GOLD_ORNAMENTED_CHALK_BRICKS)
				.add(ModBlocks.GOLD_ORNAMENTED_OVERGROWN_CHALK_BRICKS);
	}
}
